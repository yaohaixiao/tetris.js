/**
 * ============================================================
 *
 * # 模块：resize 自适应画布尺寸
 *
 * ============================================================
 *
 * ## 功能描述
 *
 * 根据当前窗口尺寸重新计算并设置游戏棋盘和预览画布的大小， 同时更新方块尺寸和字体大小等全局参数。
 *
 * ## 计算逻辑
 *
 * ### 桌面端（宽度 ≥ 480px）
 *
 * - **高度基准**：窗口高度的 90%
 * - **方块尺寸**：高度基准 ÷ 行数（向下取整）
 * - **画布宽度**：方块尺寸 × 列数
 * - **画布高度**：方块尺寸 × 行数
 * - **字体大小**：画布高度 × 3.2%
 *
 * ### 移动端（宽度 < 480px）
 *
 * - **宽度基准**：窗口宽度的 64%
 * - **高度基准**：窗口高度的 68%
 * - **方块尺寸**：取宽度基准÷列数 和 高度基准÷行数 的较小值
 * - **预览画布**：方块尺寸 × 5（正方形）
 *
 * ### 预览画布
 *
 * - **桌面端**：取窗口宽度 16% 和高度 18% 中的较小值
 * - **移动端**：方块尺寸 × 5（正方形）
 *
 * @function resize
 * @param {object} canvas - Canvas 画布管理器对象
 * @returns {void}
 */
const resize = (canvas) => {
  const { gameBoard, nextPiece, holdPiece, rows, cols } = canvas;
  const { innerWidth, innerHeight } = globalThis;
  let blockSize;
  let nextSize;
  let holdSize;

  /*
   * ============================================================
   * 桌面端（宽度 ≥ 480px）
   * ============================================================
   */
  if (innerWidth >= 480) {
    /*
     * ============================================================
     * 步骤 1：计算主画布高度基准
     * ============================================================
     *
     * 使用窗口高度的 90% 作为游戏区域高度，
     * 确保整体布局在可视区域内。
     */
    const h = innerHeight * 0.9;

    /*
     * ============================================================
     * 步骤 2：计算单个方块尺寸
     * ============================================================
     *
     * 根据总行数将高度均分，得到每个方块的像素大小。
     * 向下取整避免浮点数导致的模糊。
     */
    blockSize = Math.floor(h / rows);

    /*
     * ============================================================
     * 步骤 3：设置预览画布尺寸
     * ============================================================
     *
     * 使用窗口宽高的较小值作为限制：
     * - 最大不超过宽度的 16%
     * - 最大不超过高度的 18%
     */
    nextSize = holdSize = Math.min(innerWidth * 0.16, innerHeight * 0.18);
  } else {
    /*
     * ============================================================
     * 移动端（宽度 < 480px）
     * ============================================================
     */

    /*
     * ============================================================
     * 步骤 1：计算主画布宽度基准和高度基准
     * ============================================================
     *
     * 宽度基准：窗口宽度的 64%（留出右侧信息面板空间）
     * 高度基准：窗口高度的 68%（留出底部控制按钮空间）
     */
    const width = innerWidth * 0.64;
    const height = innerHeight * 0.68;

    /*
     * ============================================================
     * 步骤 2：计算单个方块尺寸
     * ============================================================
     *
     * 取宽度基准÷列数 和 高度基准÷行数 的较小值，
     * 确保棋盘在水平和垂直方向都不会溢出屏幕。
     */
    blockSize = Math.min(Math.floor(width / cols), Math.floor(height / rows));

    /*
     * ============================================================
     * 步骤 3：设置预览画布尺寸
     * ============================================================
     *
     * 移动端预览画布尺寸与主棋盘方块尺寸联动，
     * 预览区显示 5×5 网格（最大方块尺寸为 5 格）。
     */
    nextSize = holdSize = blockSize * 5;
  }

  /*
   * ============================================================
   * 步骤 4：计算基础字体大小
   * ============================================================
   *
   * 基于画布高度按比例缩放（3.2%），
   * 用于统一 UI 文本尺寸。
   */
  const fontSize = Math.floor(blockSize * rows * 0.032);

  /*
   * ============================================================
   * 步骤 5：应用计算结果到 Canvas
   * ============================================================
   *
   * 更新画布尺寸和方块尺寸参数。
   */
  canvas.blockSize = blockSize;
  gameBoard.width = blockSize * cols;
  gameBoard.height = blockSize * rows;
  canvas.fontSize = fontSize;

  nextPiece.width = nextSize;
  nextPiece.height = nextSize;

  holdPiece.width = holdSize;
  holdPiece.height = holdSize;
};

export default resize;
