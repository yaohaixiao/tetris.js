import parseToFullHex from '@/lib/utils/color/parse-to-full-hex.js';

/**
 * ============================================================
 *
 * # 将十六进制颜色字符串转换为 rgba 格式
 *
 * ============================================================
 *
 * 支持透明度控制，是实现玻璃质感方块的核心工具函数， 让实色能变成半透明。
 *
 * ## 转换规则
 *
 * - 输入格式：#RRGGBB（如 "#FF6B6B"）或 #RGB 短格式
 * - 输出格式：rgba(R, G, B, Alpha)
 * - 短格式自动扩展为长格式后再解析
 * - Alpha 由调用方传入，范围通常为 0 ~ 1
 *
 * ## 示例
 *
 * ```javascript
 * hexToRgba('#FF0000', 0.5); // "rgba(255, 0, 0, 0.5)"
 * hexToRgba('#fff', 0.8); // "rgba(255, 255, 255, 0.8)"
 * ```
 *
 * @function hexToRgba
 * @param {string} hex - 十六进制颜色字符串（#RRGGBB 或 #RGB）
 * @param {number} [alpha=1] - 不透明度 0-1，0 完全透明，1 完全不透明. Default is `1`
 * @returns {string} Rgba 格式的颜色字符串
 */
const hexToRgba = (hex, alpha = 1) => {
  /*
   * ============================================================
   * 步骤 1：统一为 6 位长格式
   * ============================================================
   *
   * 如果是 #RGB 短格式（长度为 4），将每位重复一次扩展
   * 为 #RRGGBB。例如：#fff → #ffffff，#0af → #00aaff
   * ============================================================
   */
  const fullHex = parseToFullHex(hex);

  /*
   * ============================================================
   * 步骤 2：提取 RGB 通道
   * ============================================================
   *
   * 使用 parseInt 将十六进制子串转为十进制整数：
   * - fullHex.slice(1, 3)：红色通道
   * - fullHex.slice(3, 5)：绿色通道
   * - fullHex.slice(5, 7)：蓝色通道
   * ============================================================
   */
  const r = Number.parseInt(fullHex.slice(1, 3), 16);
  const g = Number.parseInt(fullHex.slice(3, 5), 16);
  const b = Number.parseInt(fullHex.slice(5, 7), 16);

  /*
   * ============================================================
   * 步骤 3：拼接 rgba 字符串
   * ============================================================
   *
   * 使用模板字符串直接嵌入变量，
   * 生成 CSS rgba() 函数格式。
   * ============================================================
   */
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
};

export default hexToRgba;
