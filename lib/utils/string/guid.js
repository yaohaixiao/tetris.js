/*
 * ============================================================
 * 模块：GUID 唯一标识符生成器
 * ============================================================
 *
 * 【功能描述】
 * 生成全局唯一标识符，用于排行榜记录、实体 ID 等场景
 *
 * 【实现策略】
 * 1. 优先使用 Web Crypto API 的 randomUUID()（标准 UUID v4）
 * 2. 不支持时回退到基于时间戳 + 36 进制截取的简化方案
 *
 * 【使用示例】
 *   const id = guid();
 *   // 标准模式：'123e4567-e89b-12d3-a456-426614174000'
 *   // 回退模式：'1628496000000-k8x7n2y4-9m3p5q7r'
 * ============================================================
 */
const guid = () => {
  /*
   * ============================================================
   * 步骤 1：获取时间戳
   * ============================================================
   *
   * 【为什么需要时间戳】
   * - 作为回退方案的核心唯一性来源
   * - 同一毫秒内多次调用仍可区分（截取不同位置）
   * - 可用于排序和追溯生成时间
   * ============================================================
   */
  const timestamp = Date.now();

  /*
   * ============================================================
   * 步骤 2：时间戳转 36 进制
   * ============================================================
   *
   * 【为什么用 36 进制】
   * - 字符集：0-9 + a-z，比 10 进制/16 进制更紧凑
   * - 示例：1628496000000 → 'k8x7n2y4'
   * - 在相同信息量下，字符串更短
   * ============================================================
   */
  const RADIX_36 = timestamp.toString(36);

  /*
   * ============================================================
   * 步骤 3：生成最终 ID
   * ============================================================
   *
   * 【优先方案】crypto.randomUUID()
   *   - 符合 RFC 4122 标准（UUID v4）
   *   - 加密安全的随机数生成器（CSPRNG）
   *   - 需要安全上下文（HTTPS 或 localhost）
   *
   * 【回退方案】时间戳 + 36 进制截取
   *   - 格式：timestamp-36进制中间段-36进制后半段
   *   - 从不同位置截取，增加字符串变化性
   *   - 降低同一毫秒内的碰撞概率
   *
   * 【碰撞分析】
   *   碰撞条件：同一毫秒内多次调用，且截取到的字符串完全相同
   *   概率：在 Tetris.js 排行榜场景中，概率极低，可忽略不计
   * ============================================================
   */
  return (
    crypto?.randomUUID?.() ||
    `${timestamp}-${RADIX_36.slice(2, 10)}-${RADIX_36.slice(12, 20)}`
  );
};

export default guid;
