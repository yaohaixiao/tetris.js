/**
 * # SRS 墙踢数据（Standard Rotation System）
 *
 * 每个方块类型有两组偏移表：0→R 和 R→0 等。 索引 0-3 对应 4 个旋转状态（0, R, 2, L）。
 *
 * 格式：[offsetX, offsetY]，正数 X = 右移，正数 Y = 下移
 */

/** I 型方块的墙踢数据 */
export const KICK_I = [
  // 0→R
  [
    [0, 0],
    [-2, 0],
    [1, 0],
    [-2, -1],
    [1, 2],
  ],
  // R→2
  [
    [0, 0],
    [-1, 0],
    [2, 0],
    [-1, 2],
    [2, -1],
  ],
  // 2→L
  [
    [0, 0],
    [2, 0],
    [-1, 0],
    [2, 1],
    [-1, -2],
  ],
  // L→0
  [
    [0, 0],
    [1, 0],
    [-2, 0],
    [1, -2],
    [-2, 1],
  ],
];

// 5 格 I 型专属墙踢数据
export const KICK_I5 = [
  // 0→R
  [
    [0, 0],
    [-2, 0],
    [1, 0],
    [-2, -1],
    [1, 2],
  ],
  // R→0（逆时针回来）
  [
    [0, 0],
    [2, 0],
    [-1, 0],
    [2, 1],
    [-1, -2],
  ],
];

/** J/L/S/Z/T 型方块的墙踢数据 */
export const KICK_JLSZT = [
  // 0→R
  [
    [0, 0],
    [-1, 0],
    [-1, 1],
    [0, -2],
    [-1, -2],
  ],
  // R→2
  [
    [0, 0],
    [1, 0],
    [1, -1],
    [0, 2],
    [1, 2],
  ],
  // 2→L
  [
    [0, 0],
    [1, 0],
    [1, 1],
    [0, -2],
    [1, -2],
  ],
  // L→0
  [
    [0, 0],
    [-1, 0],
    [-1, -1],
    [0, 2],
    [-1, 2],
  ],
];

/**
 * 根据方块类型获取对应的墙踢数据
 *
 * @param {string} type - 方块类型名（'I' | 'O' | 其他）
 * @returns {number[][][] | null} 墙踢数据，O 型返回 null
 */
export const getKickData = (type) => {
  if (type === 'I') return KICK_I;
  if (type === 'O') return null;
  return KICK_JLSZT;
};
