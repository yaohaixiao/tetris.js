/**
 * # 获取当前等级的方块自动下落速度
 *
 * 根据游戏等级计算方块自动下落的时间间隔。 等级越高，下落越快（间隔越小）。
 *
 * ## 计算公式
 *
 *     step = ceil(1000 / floor(maxLevel × 0.7))
 *     speed = max(120, 1000 - (level - 1) × step)
 *
 * ## 设计思路
 *
 * - **基础速度**：等级 1 时下落间隔为 1000ms（1 秒）
 * - **平滑递增**：每升一级，间隔减少固定值 `step`
 * - **极限保护**：最低间隔为 120ms，防止速度过快导致无法操作
 * - **挑战曲线**：前 70% 等级达到极限速度，后 30% 保持极限速度考验耐力
 *
 * ## 速度示例（假设 maxLevel = 99）
 *
 *     step = ceil(1000 / floor(99 × 0.7))
 *      = ceil(1000 / 69)
 *      = ceil(14.49...)
 *      = 15
 *
 * | 等级 | 计算过程                             | 下落间隔 |
 * | ---- | ------------------------------------ | -------- |
 * | 1    | 1000 - 0×15 = 1000                   | 1000ms   |
 * | 10   | 1000 - 9×15 = 865                    | 865ms    |
 * | 50   | 1000 - 49×15 = 265                   | 265ms    |
 * | 60   | 1000 - 59×15 = 115 → max(120, 115)   | 120ms    |
 * | 99   | 1000 - 98×15 = -470 → max(120, -470) | 120ms    |
 *
 * 可以看到，大约在 60 级左右达到极限速度 120ms。
 *
 * ## 用途
 *
 * - **游戏主循环**：`startGameLoop` 中判断是否执行下落逻辑
 * - **AI 决策节奏**：AI 使用相同间隔进行决策，保持与人类玩家一致的速度感
 *
 * @function getSpeed
 * @param {object} runtime - 游戏运行时对象
 * @returns {number} 方块自动下落的时间间隔（毫秒），范围 [120, 1000]
 */
const getSpeed = (runtime) => {
  const { Store, Level } = runtime;
  const level = Store.getLevel();

  /**
   * 计算每级的速度递减步长
   *
   * 算法：达到最大等级的 70% 时，就达到极限下落速度， 剩余 30% 的等级保持在极限速度，考验玩家的耐力。
   */
  const step = Math.ceil(1000 / Math.floor(Level.max * 0.7));

  /**
   * 计算下落速度
   *
   * 基础值 1000ms，每升一级减少 step 毫秒， 最低不低于 120ms，既有挑战性又不至于无法操作。
   */
  return Math.max(120, 1000 - (level - 1) * step);
};

export default getSpeed;
