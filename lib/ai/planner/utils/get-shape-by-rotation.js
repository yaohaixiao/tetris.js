/**
 * ## 获取指定方块、指定朝向的形状矩阵
 *
 * @param {string} type - 方块类型 ('I' | 'I5' | 'T' | 'J' | 'L' | 'S' | 'Z' | 'O')
 * @param {number} rotationState - 旋转状态 (0 | 1 | 2 | 3)
 * @returns {number[][]} 对应的形状二维矩阵
 */
const getShapeByRotation = (type, rotationState) => {
  if (!TETROMINO_SHAPES[type]) {
    return undefined;
  }
  return TETROMINO_SHAPES[type][rotationState];
};

export default getShapeByRotation;
